const typeDefs = gql`

  type Query {
    hello: String!
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book

    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
 
  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    id: ID!
    born: Int
    bookCount: Int!
  }

`
const resolvers = {
  Query: {
    hello: () => { return "world" },
    bookCount: () => Book.collection.countDocuments(),
    authorCount: () => Author.collection.countDocuments(),
    allBooks: (root, args) => {
      let allbooks = Book.find({}).exec()
      if(args.author) {
        allbooks = books.filter(b => b.author === args.author)
      }
      if(args.genre) {
        allbooks = allbooks.filter(b => {
          if(b.genres.indexOf(args.genre) !== -1) {
            return b
          }
        })
      }
      return allbooks 
    },
    allAuthors: (root, args) => Author.find({})
  },
  Author: {
    bookCount: (root) => Book.find({author: root._id}).countDocuments()
  },
  Mutation: {
    addBook: async (root, { title, author, published, genres }) => {
      if(!await Author.findOne({name: author})) {
        author = new Author({ name: author })
        author = await author.save()
      }
      let newbook = new Book({ title, author, published, genres})
      return await newbook.save()
    },
    editAuthor: async (root, { name, setBornTo }) => {
      let author = await Author.findOne({ name })
      if(!author) {
        return null
      }
      const updatedAuthor = await Author.findByIdAndUpdate(author.id,
        { born: setBornTo }, { new: true })
      return updatedAuthor
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})